
#include <stdio.h>

int main() {
    // Declaración de variables dobles
    double variable1, variable2;

    // Asignación de valores
    variable1 = 10.5;
    variable2 = 2.0;

    // Mostrar resultados
    printf("Variable 1: %f\n", variable1);
    printf("Variable 2: %f\n", variable2);

    // Dividir variable1 entre variable2
    double resultado = variable1 / variable2;

    printf("Resultado de la división: %f\n", resultado);

    return 0;
}

